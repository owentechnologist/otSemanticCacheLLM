# this function creates the index in redis and returns its name:
def create_index_in_cache(cache_connection):
    index_name = 'idx_vss_lclllm_'+uid_prefix
    SCHEMA = [
        VectorField("embedding", "FLAT", {"INITIAL_CAP": 1000, "TYPE": "FLOAT32", "DIM": 768, "DISTANCE_METRIC": "COSINE"}),
    ]
    # Create the index (Index is created in the Redis Database)
    try:
        cache_connection.ft(index_name).create_index(fields=SCHEMA, definition=IndexDefinition(prefix=[(uid_prefix+":prompt:")], index_type=IndexType.HASH))
    except Exception as e:
        print(repr(e))
    return index_name

# this function executes the VSS search call against Redis
# it accepts a redis index (generated by calling connection)
def vec_search(vindex, query_vector_as_bytes):
    # Dragonfly requires a clean VECTOR_RANGE statement.
    # We yield the distance as 'knn_dist' and sort/limit it manually.
    query = (
        Query('@embedding:[VECTOR_RANGE 0.23 $knn_vec]=>{$yield_distance_as: knn_dist}')
        .sort_by('knn_dist')  # Sort from closest to furthest
        .return_fields("response_key", "knn_dist")
        .dialect(2)
    )    

    # Execute the query against Dragonfly
    res = vindex.search(query, query_params={
        'knn_vec': query_vector_as_bytes
    })

    print(f"DEBUG: Search returned {res.total} results.")
    
    # Check if any documents fell within the distance threshold
    if res.total == 0:
        print("No semantic match found within the specified range.")
        return None
        
    # By default, Dragonfly/Redis returns up to 10 items via .limit(0, 10).
    # If you need to strictly cap it at a lower number (like top 5), 
    # you can slice the array here or add `.limit(0, 5)` to the Query object.
    return res.docs

